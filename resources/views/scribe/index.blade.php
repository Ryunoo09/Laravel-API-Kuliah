        <h1 id="introduction">Introduction</h1>
<p>RESTful API untuk manajemen Posts dan Comments dengan autentikasi berbasis Laravel Sanctum.</p>
<aside>
    <strong>Base URL</strong>: <code>http://localhost</code>
</aside>
<pre><code>Dokumentasi ini menyediakan semua informasi yang diperlukan untuk menggunakan API kami.

## Autentikasi
API ini menggunakan **Bearer Token** via Laravel Sanctum. Untuk mendapatkan token, lakukan request `POST /api/v1/login` dengan email dan password Anda.

Setelah mendapatkan token, sertakan di setiap request pada header:
```
Authorization: Bearer {YOUR_AUTH_KEY}
```

## Rate Limiting
- **Login**: Maksimal 5 request per menit
- **Endpoint lainnya**: Maksimal 60 request per menit per user

&lt;aside&gt;Saat Anda scroll ke bawah, Anda akan melihat contoh kode untuk berbagai bahasa pemrograman di area gelap sebelah kanan.&lt;/aside&gt;</code></pre>

        <h1 id="authenticating-requests">Authenticating requests</h1>
<p>To authenticate requests, include an <strong><code>Authorization</code></strong> header with the value <strong><code>"Bearer {YOUR_AUTH_KEY}"</code></strong>.</p>
<p>All authenticated endpoints are marked with a <code>requires authentication</code> badge in the documentation below.</p>
<p>Dapatkan token dengan melakukan request <b>POST /api/v1/login</b> menggunakan email dan password Anda.</p>

